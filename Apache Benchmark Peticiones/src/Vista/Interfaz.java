    private void jButtonGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerarActionPerformed
        // TODO add your handling code here:
        String ruta = jTruta.getText();
        String contenidoSecuencial = jTcontenidoSecuencial.getText();
        String contenidoConcurrente = jTcontenidoConcurrente.getText();

        try {
            FileOutputStream archivo = new FileOutputStream(ruta + ".pdf");
            
            com.itextpdf.text.Font fuenteNegrita = FontFactory.getFont("Arial", 14, Font.BOLD);
            com.itextpdf.text.Font fuentedefecto = FontFactory.getFont("Times-Roman", 11);
            
            Document doc = new Document();
            PdfWriter.getInstance(doc, archivo);
            doc.open();
            doc.addTitle("PETICION(ES) SECUENCIALES Y CONCURRENTES");            
            doc.add(new Paragraph("PETICION(ES) SECUENCIALES\n\n", fuenteNegrita));
            doc.add(new Paragraph(contenidoSecuencial, fuentedefecto));
            doc.add(new Paragraph("PETICION(ES) CONCURRENTES\n\n", fuenteNegrita));
            doc.add(new Paragraph(contenidoConcurrente, fuentedefecto));
            doc.close();

            JOptionPane.showMessageDialog(null, "PDF generado correctamente");

        } catch (Exception e) {
            System.out.println("Error: " + e);
        }

    }//GEN-LAST:event_jButtonGenerarActionPerformed

